function Click(gcbo,eventdata,handles)
global VAR BLACK WHITE PLAYERCHANGE;
temp = round(get(gca,'Currentpoint'));
if PLAYERCHANGE == 1
    BLACK(VAR(1),:) = temp(2,1:2);
    Draw(PLAYERCHANGE);
    Judge(PLAYERCHANGE);
    VAR(1) = VAR(1) + 1;
    PLAYERCHANGE = 2;
else
    WHITE(VAR(2),:) = temp(2,1:2);
    Draw(PLAYERCHANGE);
    Judge(PLAYERCHANGE);
    VAR(2) = VAR(2) + 1;
    PLAYERCHANGE = 1;
